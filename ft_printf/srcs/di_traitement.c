#include"../include/ft_printf.h"

int	di_traitement(int nb)
{
	int	i;

	i = 1;
	ft_putnbr(nb);
	if (nb == -2147483648)
		return (11);
	if (nb < 0)
	{
		nb *= -1;
		i++;
	}
	while (nb > 9)
	{
		nb /= 10;
		i++;
	}
	return (i);
}
