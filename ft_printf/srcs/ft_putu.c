#include"../include/ft_printf.h"

void	ft_putu(unsigned int nb)
{
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putu(nb % 10);
	}
	else if (nb >= 0 && nb <= 9)
		ft_putchar(nb + '0');
}
