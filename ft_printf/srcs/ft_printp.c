#include "../include/ft_printf.h"

void	ft_printp(unsigned long a)
{
	char	*t;

	t = "0123456789abcdef";
	if (a > 15)
	{
		ft_printp(a / 16);
		ft_printp(a % 16);
	}
	else
		ft_putchar(t[a]);
}
